<span id="bright">Biography (French)</span></p>

<p>Jeremy Clark est professeur agrégé à l’Institut d’ingénierie des systèmes d’information de l’Université Concordia. Il est titulaire de la chaire de recherche industrielle CRSNG–Raymond Chabot Grant Thornton–Catallaxy sur les technologies de chaînes de blocs. Il a obtenu son doctorat à l’Université de Waterloo, où il a reçu une médaille d’or pour sa thèse sur la conception et le déploiement de systèmes de vote sécurisés tels que Scantegrity – premier système à vérification cryptographique utilisé dans une élection du secteur public. Il a écrit l’un des premiers articles universitaires sur la cryptomonnaie, réalisé plusieurs projets de recherche dans ce domaine et collaboré à la rédaction du premier manuel sur le sujet. Il a en outre collaboré avec plusieurs municipalités à titre d’expert de la technologie de vote et présenté des déclarations sur le Bitcoin à un comité sénatorial et au Comité permanent des finances de la Chambre des communes du Canada.</p>

